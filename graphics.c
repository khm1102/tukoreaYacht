// 주사위를 가로로 출력하는 함수
void displayAllDiceHorizontal(int dice[], int numDice) {
    for (int row = 0; row < 5; row++) { // 각 주사위 패턴의 행을 출력
        for (int i = 0; i < numDice; i++) {
            printf("%s  ", dicePatterns[dice[i] - 1][row]); // 각 주사위의 해당 행 출력
        }
        printf("\n");
    }
}



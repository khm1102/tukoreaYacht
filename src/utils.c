// ศญ๏ฟฝ๏ฟฝวฅ ลฐ๏ฟฝ๏ฟฝ ๏ฟฝึป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฯฐลณ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ/๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ รณ๏ฟฝ๏ฟฝ
void dice_S(int dice[], int keep[]) {
    int cursor = 0; // ฤฟ๏ฟฝ๏ฟฝ ๏ฟฝสฑ๏ฟฝ ๏ฟฝ๏ฟฝฤก
    int running = 1;

    while (running) {
        display_DWC(dice, keep, cursor); // ๏ฟฝึป๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ

        int key = _getch(); // ลฐ ๏ฟฝิท๏ฟฝ รณ๏ฟฝ๏ฟฝ
        if (key == ARROW) {
            key = _getch();
            if (key == LEFT) {
                cursor = (cursor == 0) ? NUM_DICE - 1 : cursor - 1; // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝฬต๏ฟฝ
            }
            else if (key == RIGHT) {
                cursor = (cursor == NUM_DICE - 1) ? 0 : cursor + 1; // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝฬต๏ฟฝ
            }
        }
        else if (key == SPACEBAR) {
            keep[cursor] = !keep[cursor]; // ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ
        }
        else if (key == ENTER) {
            if (select_confirm(dice, keep)) {
                printf("๏ฟฝึป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝูฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝิดฯด๏ฟฝ...\n");
                Sleep(1000); // 1๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
                running = 0;
            }
        }
        else if (key == ESC) {
            printf("Exiting dice selection...\n");
            Sleep(1000); // 1๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
            running = 0;
        }
    }
}
